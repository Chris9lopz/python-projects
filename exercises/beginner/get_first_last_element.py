# Exercise get the first and the last element of the example list

def get_first_last(user_list):
    first_element = user_list[0]
    last_element = user_list[-1]
    return first_element, last_element


example_list = ['Java', 'JavaScript', 'Python', 'C#', 'C++']

print(get_first_last(example_list))




